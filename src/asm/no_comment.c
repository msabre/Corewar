#include "../../includes/asm.h"

void	no_comment(t_file *ssl, const char *li)
{
	if (li[ssl->a] == '#' || li[ssl->a] == ';')
		while (li[ssl->a] && li[ssl->a] != '\n')
			ssl->a++;
}
